-- CHALLENGE APPLICATIONS (Pending Stores)
-- Instead of a separate table structure, we can reuse `stores` with status='PENDING'
-- But since it has a "comment" and "applicant_id", we might want a separate table linking to the store ID.
-- Or just add these columns to `stores` and make them nullable.
-- Let's create a separate table for the application metadata that links to the store.

CREATE TABLE public.challenge_applications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    applicant_id UUID REFERENCES public.profiles(id) ON DELETE SET NULL,
    store_id BIGINT REFERENCES public.stores(id) ON DELETE CASCADE, -- The store created from this application
    comment TEXT,
    status TEXT DEFAULT 'PENDING' CHECK (status IN ('PENDING', 'APPROVED', 'REJECTED')),
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- RLS for challenge_applications
ALTER TABLE public.challenge_applications ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can view their own applications." ON public.challenge_applications
    FOR SELECT USING (auth.uid() = applicant_id);

CREATE POLICY "Users can create applications." ON public.challenge_applications
    FOR INSERT WITH CHECK (auth.uid() = applicant_id);
